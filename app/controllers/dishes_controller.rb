class DishesController < ApplicationController
  before_action :set_dish, only: %i[update show destroy edit]

  def create
    @dish = Dish.create(dish_params)

    if @dish.save
      redirect_to dishes_path, notice: 'dish was created'
    end
  end

  def update
    @dish.update(dish_params)

    redirect_to dish_path(@dish), notice: 'dish was updated'
  end

  def destroy

    if Menu.where.not(dishes include @dish[:id]).nil?
      @dish.destroy
      redirect_to dishes_path, notice: 'dish was deleted'
    else
      redirect_to dishes_path, notice: "Dish can't be destroyed."
    end

  end

  def show
    @category = Category.find(Dish.find(params[:id]).category_id)
  end

  def index
    @menus = Menu.all
    @categories = Category.all
    @dishes = Dish.all
  end

  def new
    @dish = Dish.new
  end

  def edit
  end

  private

  def dish_params
    params.require(:dish).permit(:name, :price_type, :price, :category_id )
  end

  def set_dish
    @dish = Dish.find(params[:id])
  end
end
